package br.gov.caixa.simtx.simulador.testes.tabelas;

public class CategoriaMensagem {
	
    private int nuCategoriaMensagem;

    private String noCategoriaMensagem;

    private String deProcessoExecucao;

	public int getNuCategoriaMensagem() {
		return nuCategoriaMensagem;
	}

	public void setNuCategoriaMensagem(int nuCategoriaMensagem) {
		this.nuCategoriaMensagem = nuCategoriaMensagem;
	}

	public String getNoCategoriaMensagem() {
		return noCategoriaMensagem;
	}

	public void setNoCategoriaMensagem(String noCategoriaMensagem) {
		this.noCategoriaMensagem = noCategoriaMensagem;
	}

	public String getDeProcessoExecucao() {
		return deProcessoExecucao;
	}

	public void setDeProcessoExecucao(String deProcessoExecucao) {
		this.deProcessoExecucao = deProcessoExecucao;
	}
    
}
